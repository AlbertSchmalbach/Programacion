import math

cifra = 3.75

print(math.floor(cifra)) # baja
print(math.ceil(cifra)) # alta

numeros = [20, 2.5, 15, 30, 5]

# Sumatorio mejorado
print(math.fsum(numeros))

# Truncamiento
print(math.trunc(cifra))

# Potencias y raices
print(math.pow(2, 3))
print(math.sqrt(25))

# Constantes
print(math.pi)
print(math.e)